# 批量生成优化后的章节
for chapter in chapters:
    print(f"\n{'='*60}")
    print(f"正在优化第{chapter['chapter_num']}章：{chapter['title']}")
    print(f"{'='*60}")
    
    prompt = f"""
你是一位资深的网络小说作家，擅长写古风悬疑和玄幻题材。请按照以下要求生成章节：

{chapter['task']}

字数要求：约2000字
语言风格：古风简练，悬疑惊悚，节奏紧凑
结构要求：有场景描写，有对话，有伏笔，每章结尾有钩子
"""
    
    payload = {
        "model": "deepseek-chat",
        "messages": [
            {"role": "system", "content": "你是一位资深的网络小说作家，擅长写古风悬疑和玄幻题材。你的文字风格简练有力，情节紧凑，善于制造悬念和伏笔。"},
            {"role": "user", "content": prompt}
        ],
        "max_tokens": 2500,
        "temperature": 0.7
    }
    
    response = requests.post(
        f"{api_base}/chat/completions",
        headers={"Authorization": f"Bearer {api_key}", "Content-Type": "application/json"},
        json=payload,
        timeout=180
    )
    
    if response.status_code == 200:
        result = response.json()
        content = result["choices"][0]["message"]["content"]
        
        file_path = f"{project_dir}\\正文\\第{chapter['chapter_num']}章_{chapter['title']}.txt"
        with open(file_path, 'w', encoding='utf-8') as f:
            f.write(content)
        
        print(f"✓ 第{chapter['chapter_num']}章优化完成！")
        print(f"  文件: {file_path}")
        print(f"\n  预览:\n{content[:300]}...")
    else:
        print(f"✗ 请求失败: {response.status_code}")

print(f"\n{'='*60}")
